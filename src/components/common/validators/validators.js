export const emailValid = (value) => { 
    if (!/^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/i.test(value) && !!value) {
        return 'Проверьте правильность введенных данных'
    }
}
 
export const phoneValid = (value) => {
    if(!/^\(?([0-9]{3})\)?[-. ]?([0-9]{3})[-. ]?([0-9]{4})$/i.test(value) && !!value){
        return 'Проверьте правильность введенных данных'
    }
};  
 